package com.contrastsecurity.ide.eclipse.ui.internal.model;

import com.contrastsecurity.ide.eclipse.core.Constants;
import com.contrastsecurity.models.Application;

public class ApplicationUIAdapter implements IContrastLabelProvider {

	private Application application;
	private String name;

	public ApplicationUIAdapter(Application application, String name) {
		super();
		this.application = application;
		this.name = name;
	}

	public Application getApplication() {
		return application;
	}

	public void setApplication(Application application) {
		this.application = application;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		if (application != null) {
			return application.getId();
		}
		return Constants.ALL_APPLICATIONS;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((getId() == null) ? 0 : getId().hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ApplicationUIAdapter other = (ApplicationUIAdapter) obj;
		if (getId() == null) {
			if (other.getId() != null)
				return false;
		} else if (!getId().equals(other.getId()))
			return false;
		return true;
	}



}
